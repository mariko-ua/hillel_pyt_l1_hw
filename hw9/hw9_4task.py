# Дано два списки однакової довжини. Необхідно створити з них словник таким чином,
# щоб елементи першого списку були ключами, а елементи другого відповідно значеннями нашого словника.

list_first = [1, 2, 3, 4, 5, 6, 7]
list_second = ['a', 'b', 'c', 'd', 'e', 'f', 'g']

dictionary = {
    first: second
    for (first, second)
    in zip(list_first, list_second)
}
print(dictionary)